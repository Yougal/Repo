	private int romanToDecimal(String str) {
		int res= 0;
		HashMap<String,Integer> items= new HashMap<>();
		items.put("I", 1);
		items.put("V", 5);
		items.put("X", 10);
		items.put("L", 50);
		items.put("C", 100);
		items.put("D", 500);
		items.put("M", 1000);
		int lastNumber = Integer.MAX_VALUE;
		for(int i=0;i<str.length();){
			int value = items.get(String.valueOf(str.charAt(i)));
			if(value>lastNumber){
				res=(res-lastNumber)+(value-lastNumber);
				lastNumber=value;
				i++;
			}else{
				for(;i<str.length();i++){
					value = items.get(String.valueOf(str.charAt(i)));
					if(value<=lastNumber){
						res+=value;
						lastNumber=value;
					}else{
						break;
					}
				}
			}
		}
	
		return res;
	}
